// =============================================================================
// Descriptions.cpp — Session E 実装
//
// 全34パラメータの解説文を定義する。
// Web 版 web/src/data/descriptions.ts の summary + detail を統合し、
// 「数値の意味」と「人間の感覚での意味」を一つの文字列にまとめた。
// =============================================================================
#include "Descriptions.h"
#include "Parameters.h"

namespace synth {

juce::String getDescription(const juce::String& paramId)
{
    // -----------------------------------------------------------------------
    // オシレーター
    // -----------------------------------------------------------------------
    if (paramId == ParamID::OscWaveform)
        return
            "【波形】 オシレーターが生成する基本波形の種類。音色の根幹を決める。\n"
            "\n"
            "Sine（サイン波）: 倍音なし。フルート・口笛のような澄んだ柔らかい音。\n"
            "Sawtooth（ノコギリ波）: 全倍音を含む。明るく華やかでシンセらしい音。\n"
            "Square（矩形波）: 奇数倍音のみ。中空でユニークな、ゲーム機音風の音。\n"
            "Triangle（三角波）: サイン波に近いが少し芯のある柔らかい音。";

    if (paramId == ParamID::OscDetune)
        return
            "【デチューン】 基準ピッチからのずれをセント（1/100半音）単位で指定。\n"
            "\n"
            "数値の意味: 0=基準ピッチ。+値=高く、−値=低く。\n"
            "±100cent=半音、±1200cent=1オクターブのずれ。\n"
            "\n"
            "感覚的な効果: 複数のオシレーターをわずかにずらすとコーラス効果で音に厚みが生まれる。\n"
            "ユニゾン使用時は「Unison Detune」で自動的に広げられるため、\n"
            "このパラメータはオシレーター全体のピッチ微調整に使う。";

    if (paramId == ParamID::OscOctave)
        return
            "【オクターブ】 オシレーターの基準音高を1オクターブ単位でシフト。\n"
            "\n"
            "数値の意味: 0=鍵盤通りの音高。+1=1オクターブ高く（周波数2倍）、+2=2倍さらに高く。\n"
            "−1=1オクターブ低く（周波数1/2）、−2=さらに低く。\n"
            "\n"
            "感覚的な効果: ベース音色は−1〜−2が定番。リード音色は0〜+1が多い。";

    if (paramId == ParamID::OscLevel)
        return
            "【オシレーター音量】 オシレーターの出力レベル（0〜1）。\n"
            "\n"
            "数値の意味: 1.0=フルレベル。0.0=完全に無音（ノイズだけが鳴る）。\n"
            "\n"
            "感覚的な効果: ノイズと混ぜて使うときにバランスを調整する。\n"
            "ハイハット風の音色はOsc=0、Noise=1で作るのが基本。";

    // -----------------------------------------------------------------------
    // ノイズ
    // -----------------------------------------------------------------------
    if (paramId == ParamID::NoiseType)
        return
            "【ノイズ種別】 ノイズの周波数分布を選択する。\n"
            "\n"
            "White（ホワイトノイズ）: 全帯域均一。「サー」という鋭く明るいノイズ。\n"
            "シンバル・ハイハット・テレビの砂嵐風に適している。\n"
            "\n"
            "Pink（ピンクノイズ）: 低音寄り（−3dB/oct）。「シャー」という暖かいノイズ。\n"
            "滝の音・雨音・パッドのテクスチャに向く。";

    if (paramId == ParamID::NoiseLevel)
        return
            "【ノイズ音量】 ノイズの出力レベル（0〜1）。0で無効。\n"
            "\n"
            "数値の意味: 0.0=ノイズなし。0.05〜0.2=少量で息感やテクスチャを追加。1.0=ノイズのみ。\n"
            "\n"
            "感覚的な効果: 少量のノイズをオシレーターに混ぜると「アナログ感」が出る。\n"
            "多く混ぜると打楽器的な音色に近づく。";

    // -----------------------------------------------------------------------
    // フィルター
    // -----------------------------------------------------------------------
    if (paramId == ParamID::FilterType)
        return
            "【フィルター種別】 どの周波数帯域を通過させるかを選択する。\n"
            "\n"
            "LP（ローパス）: 低い周波数を通し、高い周波数を削る。\n"
            "→ 音がこもって温かみが増す。シンセ音色作りで最も多用される。\n"
            "\n"
            "HP（ハイパス）: 高い周波数を通し、低い周波数を削る。\n"
            "→ 音が細く鋭くなる。ベース成分を削ってスッキリさせるのに使う。\n"
            "\n"
            "BP（バンドパス）: カットオフ周辺の帯域だけを通す。\n"
            "→ 「電話越し」「ラジオ越し」のような独特な音になる。";

    if (paramId == ParamID::FilterCutoff)
        return
            "【カットオフ周波数】 フィルターが効き始める基準周波数（20Hz〜20kHz）。\n"
            "\n"
            "数値の意味: ローパス時はこの周波数より上の成分を減衰させる。\n"
            "スケールは対数（500Hzが中央）。低音域の細かい調整がしやすい。\n"
            "\n"
            "感覚的な効果: 下げると音がこもって暗くなる。上げると明るくなる。\n"
            "カットオフを動かすだけで劇的に音色が変わる、シンセ最重要パラメータのひとつ。";

    if (paramId == ParamID::FilterReso)
        return
            "【レゾナンス（Q値）】 カットオフ周波数付近の帯域を強調する度合い（0.1〜30）。\n"
            "\n"
            "数値の意味: 値が大きいほどカットオフ付近が鋭く増幅される。\n"
            "高すぎると自己発振（フィルター自体が音を出す）することがある。\n"
            "\n"
            "感覚的な効果: 低い値ではなめらか。中程度で「キュッ」というアナログ感が加わる。\n"
            "高い値ではピーッという共鳴音になり、独特のシンセ感が強く出る。";

    // -----------------------------------------------------------------------
    // フィルターエンベロープ
    // -----------------------------------------------------------------------
    if (paramId == ParamID::FEnvAttack)
        return
            "【フィルターEnv Attack】 フィルターエンベロープのアタック時間（0.001〜5秒）。\n"
            "\n"
            "数値の意味: カットオフが動き始めてから最大値に達するまでの時間。\n"
            "\n"
            "感覚的な効果: 短く=立ち上がりがシャープ。長く=ゆっくりと音が開いていく（フィルタースイープ）。";

    if (paramId == ParamID::FEnvDecay)
        return
            "【フィルターEnv Decay】 フィルターエンベロープのディケイ時間（0.001〜5秒）。\n"
            "\n"
            "数値の意味: カットオフが最大値からサスティンレベルへ下がるまでの時間。\n"
            "\n"
            "感覚的な効果: プラック系の「最初だけ明るい」音色はここで調整する。";

    if (paramId == ParamID::FEnvSustain)
        return
            "【フィルターEnv Sustain】 キーを押し続けたときのフィルター変調量の維持レベル（0〜1）。\n"
            "\n"
            "数値の意味: 1.0=フィルターは最大に開いたまま維持。0.0=元のカットオフ値まで戻る。\n"
            "\n"
            "感覚的な効果: プラック音色は0付近、リード音色は中〜高めが多い。";

    if (paramId == ParamID::FEnvRelease)
        return
            "【フィルターEnv Release】 ノートを離してからカットオフが戻るまでの時間（0.001〜5秒）。\n"
            "\n"
            "数値の意味: リリース時にフィルター変調がゼロへ戻る速度。\n"
            "\n"
            "感覚的な効果: Ampエンベロープのリリースと組み合わせて余韻の音色変化を作る。";

    if (paramId == ParamID::FEnvAmount)
        return
            "【フィルターEnv Amount】 フィルターエンベロープがカットオフに与える影響の強さ（−1〜+1）。\n"
            "\n"
            "数値の意味: 0=フィルターEnv無効。+方向=ノートオン時にカットオフが上昇（明るく開く）。\n"
            "−方向=ノートオン時にカットオフが下降（閉じてから戻る）。\n"
            "\n"
            "感覚的な効果: 1.0で約5オクターブ分の周波数変化。「シュワッ」という音色変化はここで作る。";

    // -----------------------------------------------------------------------
    // アンプエンベロープ
    // -----------------------------------------------------------------------
    if (paramId == ParamID::AmpAttack)
        return
            "【Amp Attack】 ノートを押した瞬間から最大音量に達するまでの時間（0.001〜5秒）。\n"
            "\n"
            "数値の意味: 0.001秒=瞬時に立ち上がる。5秒=5秒かけてゆっくり大きくなる。\n"
            "\n"
            "感覚的な効果: 短い=ピアノ・プラックのような鋭い音。長い=パッドのようにじわじわ大きくなる音。";

    if (paramId == ParamID::AmpDecay)
        return
            "【Amp Decay】 最大音量に達した後、サスティンレベルまで下がるまでの時間（0.001〜5秒）。\n"
            "\n"
            "数値の意味: 短い=アタック直後すぐにサスティンレベルへ。長い=ゆっくり下がる。\n"
            "\n"
            "感覚的な効果: サスティンが0の場合、ディケイが実質的な音の長さになる（プラック等）。";

    if (paramId == ParamID::AmpSustain)
        return
            "【Amp Sustain】 キーを押し続けている間に維持される音量レベル（0〜1）。\n"
            "\n"
            "数値の意味: 1.0=最大音量を維持（オルガン的）。0.0=キー押下中でも音がフェードアウト。\n"
            "\n"
            "感覚的な効果: 「レベル」なので時間ではなく音量比率で指定する点に注意。";

    if (paramId == ParamID::AmpRelease)
        return
            "【Amp Release】 ノートを離してから音量がゼロになるまでの時間（0.001〜5秒）。\n"
            "\n"
            "数値の意味: 短い=キーを離した瞬間にほぼ止まる。長い=ゆっくりと余韻が消える。\n"
            "\n"
            "感覚的な効果: ピアノのサスティンペダルに似た残響感を再現できる。";

    // -----------------------------------------------------------------------
    // LFO
    // -----------------------------------------------------------------------
    if (paramId == ParamID::LfoWaveform)
        return
            "【LFO波形】 LFOがどのような形で変調するかを決める波形。\n"
            "\n"
            "Sine: なめらかに上下する自然なビブラート。最もよく使われる。\n"
            "Sawtooth: 徐々に上がり急に下がる。鋭いうねり感。\n"
            "Square: 二値を切り替えるスイッチング。トリルのような効果。\n"
            "Triangle: サイン波より直線的にスイープする変調。";

    if (paramId == ParamID::LfoRate)
        return
            "【LFOレート】 LFO（低周波オシレーター）が1秒間に何サイクル振動するかの速度（0.1〜20Hz）。\n"
            "\n"
            "数値の意味: 0.1Hz=10秒に1サイクルのゆっくりした揺れ。20Hz=非常に速い揺れ。\n"
            "\n"
            "感覚的な効果: 低い値=ゆったりしたビブラートやトレモロ。\n"
            "中程度（5Hz）=自然な楽器のビブラート。高い値=金属的な変調感。";

    if (paramId == ParamID::LfoDepth)
        return
            "【LFO深さ】 変調の強さ（0〜1）。0でLFO無効、1で最大変調量。\n"
            "\n"
            "数値の意味: ターゲット（ピッチ・フィルター・アンプ）によって最大変調量の大きさが異なる。\n"
            "\n"
            "感覚的な効果: 0に近い=わずかな揺れ（自然なビブラート）。\n"
            "1に近い=ドラマチックなピッチ変化やフィルタースイープが起きる。";

    if (paramId == ParamID::LfoTarget)
        return
            "【LFOターゲット】 LFOが変調する先を選択する。\n"
            "\n"
            "Pitch: 音の高さを周期的に揺らす → ビブラート効果。\n"
            "ゆっくり・浅め=歌手のビブラート。速め・深め=シンセリードのトリル。\n"
            "\n"
            "Filter: フィルターのカットオフを周期的に動かす → ワウ・ウォブル効果。\n"
            "ゆっくり=アナログ風スイープ。速め=ダブステップ的なウォブルベース。\n"
            "\n"
            "Amp: 音量を周期的に変化させる → トレモロ効果。\n"
            "ゆっくり=ローズピアノ風の揺らぎ。速め・深め=リズミックなゲート効果。";

    // -----------------------------------------------------------------------
    // ドライブ
    // -----------------------------------------------------------------------
    if (paramId == ParamID::DriveType)
        return
            "【ドライブ種別】 歪みの性格を選ぶ。\n"
            "\n"
            "Soft（ソフトクリップ）: tanh関数で波形を「丸める」歪み。\n"
            "倍音は穏やかに増え、暖かいオーバードライブ感が出る。\n"
            "ギターアンプ・真空管・テープサチュレーションに相当。\n"
            "\n"
            "Hard（ハードクリップ）: 一定の閾値で波形を「カット」する歪み。\n"
            "奇数倍音を強く含み、激しいディストーション・ファズに相当。\n"
            "攻撃的でザラついた音になる。";

    if (paramId == ParamID::DriveAmount)
        return
            "【ドライブ量】 歪みの強さ（0〜1）。0で無効、1で最大限歪む。\n"
            "\n"
            "数値の意味: 0.0=入力スルー。0.2〜0.4=軽いサチュレーション。0.6〜0.8=はっきりした歪み。\n"
            "\n"
            "感覚的な効果: スペクトラム表示で倍音が増えるのが見える。\n"
            "音に「厚み」「前に出る感じ」を加えたいときに上げる。";

    if (paramId == ParamID::DriveMix)
        return
            "【ドライブMix】 原音と歪み音のバランス（0=原音のみ、1=歪み音のみ）。\n"
            "\n"
            "数値の意味: 0.0=ドライブ無効（Amount関係なく）。\n"
            "0.3前後=原音に歪みを少し加えるパラレルディストーション風。\n"
            "\n"
            "感覚的な効果: Mixを低くすると原音の明瞭さを保ちつつ歪みを足せる。";

    // -----------------------------------------------------------------------
    // ディレイ
    // -----------------------------------------------------------------------
    if (paramId == ParamID::DelayTime)
        return
            "【ディレイタイム】 入力音が遅れて出てくるまでの時間（0.01〜2秒）。\n"
            "\n"
            "数値の意味: 0.05〜0.1秒=ダブリング（音の厚み付け）。\n"
            "0.25〜0.5秒=典型的なやまびこ・エコー。1秒以上=リズムを刻むパターン効果。\n"
            "\n"
            "感覚的な効果: BPMに合わせて設定すると曲に馴染みやすい（4分音符=60/BPM 秒）。";

    if (paramId == ParamID::DelayFeedback)
        return
            "【フィードバック】 ディレイの出力をどれだけ入力に戻すか（0〜0.95）。\n"
            "\n"
            "数値の意味: 0=一度だけ鳴って消える。0.3〜0.5=数回繰り返して減衰。0.7〜0.9=長く繰り返す。\n"
            "\n"
            "感覚的な効果: 安全のため上限を0.95にロック（1以上だと発振）。\n"
            "高くするほど空間的な広がりが強くなる。";

    if (paramId == ParamID::DelayMix)
        return
            "【ディレイMix】 原音とディレイ音のバランス（0=原音のみ、1=ディレイ音のみ）。\n"
            "\n"
            "数値の意味: 0.0=ディレイ無効。0.3前後=控えめなディレイ感。0.5=半々。\n"
            "\n"
            "感覚的な効果: 0にすることでディレイをバイパスできる。\n"
            "通常は0.1〜0.3程度の控えめな値が自然に聞こえることが多い。";

    // -----------------------------------------------------------------------
    // リバーブ
    // -----------------------------------------------------------------------
    if (paramId == ParamID::ReverbDecay)
        return
            "【リバーブ Decay】 残響が消えるまでの時間（0.1〜10秒）。空間の「広さ」に対応。\n"
            "\n"
            "数値の意味: 0.3〜0.8秒=小さな部屋。1〜2秒=スタジオ・ライブハウス。\n"
            "3〜5秒=教会・コンサートホール。5秒以上=非現実的な広がり。\n"
            "\n"
            "感覚的な効果: パッド系は長め（3〜5秒）、リード系は短め（0.5〜1.5秒）が定番。";

    if (paramId == ParamID::ReverbMix)
        return
            "【リバーブ Mix】 原音と残響のバランス（0=原音のみ、1=残響のみ）。\n"
            "\n"
            "数値の意味: 0.0=リバーブ無効。0.1〜0.3=ほんのり残響。0.5〜0.7=はっきりした残響。\n"
            "\n"
            "感覚的な効果: 0にすることでリバーブをバイパスできる。\n"
            "過度に上げると音がぼやけるため、通常は0.2〜0.4程度が扱いやすい。";

    // -----------------------------------------------------------------------
    // Voice（ユニゾン + グライド）
    // -----------------------------------------------------------------------
    if (paramId == ParamID::UnisonCount)
        return
            "【ユニゾン数】 1つのノートで重ねるオシレーターの数（1〜8）。\n"
            "\n"
            "数値の意味: 1=通常の単一オシレーター。2〜3=控えめな厚み（コーラス効果）。\n"
            "4〜6=スーパーソウなど分厚いシンセサウンド。7〜8=非常に分厚い（CPU負荷大）。\n"
            "\n"
            "感覚的な効果: 次のノートオンから反映される。\n"
            "「Unison Detune」とセットで調整してコーラスの広がり感を決める。";

    if (paramId == ParamID::UnisonDetune)
        return
            "【ユニゾンデチューン】 重ねた各オシレーターをどれだけ広げるか（0〜100セント）。\n"
            "\n"
            "数値の意味: 0=すべてのOscが同じピッチ（ユニゾンが意味をなさない）。\n"
            "10〜20=自然なコーラス。30〜50=スーパーソウらしい派手な広がり。80〜100=不協和な特殊効果。\n"
            "\n"
            "感覚的な効果: ユニゾン数が多いほど各Oscはこの範囲に均等に分布する。";

    if (paramId == ParamID::Glide)
        return
            "【グライド（ポルタメント）】 ノート切替時にピッチが滑らかに移行する時間（0〜2秒）。\n"
            "\n"
            "数値の意味: 0=瞬時にピッチが変わる（通常のOn/Off）。\n"
            "0.05〜0.2秒=短いグライド（リードでよく使う）。\n"
            "0.3〜0.8秒=はっきりした「滑る」感じ。1秒以上=大げさなエフェクト的グライド。\n"
            "\n"
            "感覚的な効果: 前ノートが鳴っていなくても最後に押した音の周波数から滑り出す。";

    // -----------------------------------------------------------------------
    // マスター
    // -----------------------------------------------------------------------
    if (paramId == ParamID::MasterVolume)
        return
            "【マスター音量】 シンセサイザー全体の出力音量（0〜1）。\n"
            "\n"
            "数値の意味: 0.0=完全に無音。0.5=標準的な音量（デフォルト値）。\n"
            "1.0=最大音量（大きすぎるとクリッピングが生じる場合がある）。\n"
            "\n"
            "感覚的な効果: すべての音が通るマスターフェーダー。全体のバランス調整に使う。";

    return {};
}

} // namespace synth
